title('Second Vector Of 4000 Samples (half second) Of Yes'); 
xlabel('Samples----->');ylabel('Magnitude'); 
n = size(yes); 
Fs = 8000; 
ts = 1/Fs; 
tmax = (n-1)*ts; 
t = 0:ts:tmax; 
figure(4); 
plot(t,yes); 
title('Yes Speech Signal (8000Samples/sec)'); 
xlabel('Time In Seconds----->');ylabel('Magnitude');  
ts1 = 1/Fs; 
tmax1 = ((n/2)-1)*ts1; 
t1 = 0:ts1:tmax1; 
