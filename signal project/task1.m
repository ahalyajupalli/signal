 r = audiorecorder(8000,16,1); 
disp('Start speaking.') 
recordblocking(r,1); 
disp('End of Recording.'); 
play(r); 
yes = getaudiodata(r);  
g = audiorecorder(8000,16,1); 
disp('Start speaking.') 
recordblocking(g,1); 
disp('End of Recording.'); 
play(g); 
no = getaudiodata(g);  
yes1 = yes(1:4000); 
yes2 = yes(4001:8000);  
no1 = no(1:4000); 
no2 = no(4001:8000);  
figure(1); 
plot(yes); 
title('Yes Speech Signal (8000Samples/sec)'); 
xlabel('Samples---->');ylabel('Magnitude');  
figure(2); 
plot(yes1); 
title('One Vector Of 4000 Samples (half second) Of Yes'); 
xlabel('Samples----->');ylabel('Magnitude');  
figure(3); 
